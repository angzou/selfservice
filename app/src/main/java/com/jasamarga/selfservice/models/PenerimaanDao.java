public class PenerimaanDao {

    private String name;
    private String npp;
    private String reporting_name;
    private int value_penerimaan;
    private String nama;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNpp() {
        return npp;
    }

    public void setNpp(String npp) {
        this.npp = npp;
    }

    public String getReporting_name() {
        return reporting_name;
    }

    public void setReporting_name(String reporting_name) {
        this.reporting_name = reporting_name;
    }

    public int getValue_penerimaan() {
        return value_penerimaan;
    }

    public void setValue_penerimaan(int value_penerimaan) {
        this.value_penerimaan = value_penerimaan;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

}
